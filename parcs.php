                <p>Le château de la Buzine doit sa renommée à Marcel Pagnol. L’ayant acheté avec le projet d’y ouvrir une véritable Cité du cinéma, l’écrivain eut la surprise en arrivant sur place de reconnaître l’édifice qu’il longeait en famille sur le chemin de la Treille, et qui deviendrait – et restera pour toujours – « Le Château de ma mère ».</p>
