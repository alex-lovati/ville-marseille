                <h2>Le jardin du Port Antique</h2>
                <p>Le site du Port Antique (dénommé Jardin des vestiges à son ouverture au public en 1983) présente les résultats des premières grandes fouilles archéologiques urbaines réalisées en France.
                    Les vestiges, révélés en 1967 sur le chantier de construction du Centre Bourse, sont des témoins historiques majeurs. Le site se structure autour de la voie romaine, des remparts grecs et autour du Port Antique où a été découverte la plus grande épave visible au monde. Cette voie romaine longée par des monuments funéraires pénètre dans la ville par une entrée monumentale.</p>
