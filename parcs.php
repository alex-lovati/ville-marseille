                <h2>Parc du Palais Longchamp</h2>
                <p>Le Parc Longchamp, s’est constitué au fil du temps, par la stratification des différents projets, imaginés ou réalisés sur le site, depuis le XIXe siècle. Le parc Longchamp, le parc Borély, le jardin de la Magalone et le parc du 26ème centenaire, ont obtenu en 2005 le label « jardin remarquable » décerné par le Ministère de la Culture. Ce fut un Jardin zoologique, de 1854 à 1987.</p>
